// push()// 向数组末尾添加元素。
// pop()// 移除数组的最后一个元素。
// shift()// 移除数组的第一个元素。
// unshift()// 向数组开头添加元素。
// concat()// 合并两个或多个数组。
// slice()// 返回数组的一部分（浅拷贝）。
// // splice()// 添加或删除数组中的元素。
// map()// 对数组中的每个元素执行回调函数，返回新数组。
// filter()// 过滤数组中的元素，返回符合条件的新数组。
// reduce()// 对数组中的每个元素执行回调函数，累积为单个值。
// forEach()// 遍历数组中的每个元素。
// find()// 返回数组中第一个满足条件的元素。
// findIndex()// 返回数组中第一个满足条件的元素的索引。
// includes()// 判断数组是否包含某个值。
// indexOf()// 返回数组中某个值的第一个匹配项的索引。
// join()// 将数组的所有元素连接成一个字符串。
// reverse()// 反转数组中的元素顺序。
// sort()// 对数组的元素进行排序。
// flat()// 将嵌套数组“拉平”到指定深度。
// flatMap()// 先对数组中的每个元素执行回调函数，然后将结果“拉平”一层。